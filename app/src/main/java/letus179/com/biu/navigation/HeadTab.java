package letus179.com.biu.navigation;

/***
 * 头部Tab属性
 *
 */
public class HeadTab {

    // 头部名称
    private String name;

    public HeadTab(String name) {
        super();
        this.setName(name);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
